export function translate(name) {
  const upper = name.trim().toUpperCase();
  if (TEAM_NAME_MAP[upper]) return TEAM_NAME_MAP[upper];
  if (TEAM_ALIASES[upper]) return TEAM_ALIASES[upper];
  const norm = normalizeName(name);
  for (const [es, en] of Object.entries(TEAM_NAME_MAP)) {
    const esNorm = normalizeName(es);
    if (norm === esNorm || norm.includes(esNorm) || esNorm.includes(norm)) return en;
  }
  for (const [alias, en] of Object.entries(TEAM_ALIASES)) {
    const aNorm = normalizeName(alias);
    if (norm === aNorm || norm.includes(aNorm) || aNorm.includes(norm)) return en;
  }
  return name.trim();
}

export function normalizeName(name) {
  return name.trim()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/\bfc\b/g, '').replace(/\bcf\b/g, '').replace(/\bsc\b/g, '')
    .replace(/\bclub\b/gi, '').replace(/\bde\b/gi, '')
    .replace(/\s+/g, ' ').trim();
}

export function matchTeam(fixtureTeamName, searchTerm) {
  const fNorm = normalizeName(fixtureTeamName);
  const sNorm = normalizeName(searchTerm);
  if (fNorm === sNorm) return 3;
  if (fNorm.includes(sNorm) || sNorm.includes(fNorm)) return 2;
  const sAlias = TEAM_ALIASES[searchTerm.trim().toUpperCase()];
  if (sAlias && normalizeName(fixtureTeamName).includes(normalizeName(sAlias))) return 2;
  const sTranslated = translate(searchTerm);
  if (sTranslated !== searchTerm.trim()) {
    const tNorm = normalizeName(sTranslated);
    if (fNorm === tNorm) return 3;
    if (fNorm.includes(tNorm) || tNorm.includes(fNorm)) return 2;
  }
  const fWords = fNorm.split(' ').filter(w => w.length > 2);
  const sWords = sNorm.split(' ').filter(w => w.length > 2);
  const shared = fWords.filter(w => sWords.includes(w));
  if (shared.length > 0 && sWords.length > 0 && (shared.length / sWords.length) >= 0.5) return 1;
  return 0;
}
